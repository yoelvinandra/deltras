<script>
/* ── Filter dropdown ── */
const filterBtn  = document.getElementById('filterBtn');
const filterDrop = document.getElementById('filterDrop');

filterBtn.addEventListener('click', e => {
  e.stopPropagation();
  const isOpen = filterDrop.classList.toggle('open');
  filterBtn.classList.toggle('open', isOpen);
  filterBtn.setAttribute('aria-expanded', isOpen);
});
document.addEventListener('click', () => {
  filterDrop.classList.remove('open');
  filterBtn.classList.remove('open');
  filterBtn.setAttribute('aria-expanded', false);
});
filterDrop.addEventListener('click', e => {
  if (e.target.matches('a')) {
    e.preventDefault();
    filterDrop.querySelectorAll('a').forEach(a => a.classList.remove('active'));
    e.target.classList.add('active');
    filterDrop.classList.remove('open');
    filterBtn.classList.remove('open');
    filterBtn.setAttribute('aria-expanded', false);
    applyFilter(e.target.dataset.cat);
  }
});

function applyFilter(cat) {
  document.querySelectorAll('#newsGrid .news-card').forEach(card => {
    card.style.display = (cat === 'all' || card.dataset.cat === cat) ? '' : 'none';
  });
}

/* ── Search ── */
document.getElementById('searchInput').addEventListener('input', function() {
  const q = this.value.trim().toLowerCase();
  document.querySelectorAll('#newsGrid .news-card').forEach(c => {
    c.style.display = (!q || c.dataset.title.toLowerCase().includes(q)) ? '' : 'none';
  });
});

/* ── Pagination ── */
let cur = 1, total = 10;

function renderPager() {
  document.querySelectorAll('.pg[data-p]').forEach(b => {
    b.classList.toggle('active', +b.dataset.p === cur);
  });
  const prev = document.getElementById('btnPrev');
  const next = document.getElementById('btnNext');
  prev.disabled = cur <= 1;
  prev.classList.toggle('on', cur > 1);
  next.disabled = cur >= total;
  next.classList.toggle('on', cur < total);
}

document.querySelectorAll('.pg[data-p]').forEach(b => {
  b.addEventListener('click', () => { cur = +b.dataset.p; renderPager(); });
});
document.getElementById('btnPrev').addEventListener('click', () => {
  if (cur > 1) { cur--; renderPager(); }
});
document.getElementById('btnNext').addEventListener('click', () => {
  if (cur < total) { cur++; renderPager(); }
});
</script>